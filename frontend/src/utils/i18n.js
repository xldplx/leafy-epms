export function useTranslation() {
    const [lang, setLangState] = useState(() => localStorage.getItem(STORAGE_KEY) || 'en');

    useEffect(() => {
        const handler = () => setLangState(localStorage.getItem(STORAGE_KEY) || 'en');
        window.addEventListener('epms-lang-change', handler);
        return () => window.removeEventListener('epms-lang-change', handler);
    }, []);

    const setLang = useCallback((newLang) => {
        localStorage.setItem(STORAGE_KEY, newLang);
        setLangState(newLang);
        window.dispatchEvent(new Event('epms-lang-change'));
    }, []);

    const t = useCallback((key) => {
        return translations[lang]?.[key] ?? translations['en']?.[key] ?? key;
    }, [lang]);

    return { t, lang, setLang };
}

export function getLang() { return localStorage.getItem(STORAGE_KEY) || 'en'; }
export function getT(key) {
    const lang = getLang();
    return translations[lang]?.[key] ?? translations['en']?.[key] ?? key;
}